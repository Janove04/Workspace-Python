# Estas son algunas funciones útiles

# Redondeo
print(round(4.6546))
# Valor Absoluto
print(abs(-84))
# Crear una lista
lista = [1, 4, 7, 2, 6, 34, 62, 13, 56, 24]
print(lista)
# Minimo de la lista
print(min(lista))
